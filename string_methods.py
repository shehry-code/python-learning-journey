my_str = '       hello WORDL'

print("Uppercase string : ", my_str.upper())       # The function upper() change the case of the string to uppercase

print("Lowercase string : ", my_str.lower())        # The funciton lower() change the case of the string to lowercase

print('after using strip ftn :', my_str.strip())     # The strip function will remove the white spaces from the string if no argument is pass to it

print("relace string : ", my_str.replace('WORDL', 'world'))     # Replace function will repalce the part of the string with 2nd argument you send to it

